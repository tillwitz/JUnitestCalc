package Exemplos;

public class Calculadora {
    public double somar(double x, double y){
        return x + y;
    }
    public double subtracao(double x , double y){
        return x - y;
    }
    public int divisao(int x , int y){
        return x / y;
    }
    public int multi(int x, int y){
        return x * y;
    }
}
